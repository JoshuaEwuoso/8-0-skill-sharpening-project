function shoutForOfLoop(array) {
  let forOf = [];
  for (const loud of array) {
    forOf.push(loud + "!");
  }
return forOf
}

/**
 * sumArray()
 * ---------------------
 * Returns the sum of all values in the array.
 * @param {number[]} array
 * @returns {number}
 *
 * EXAMPLE:
 *  sumArray([ 10, 0, 10, 11 ]);
 *  //> 31
 */
function sumArray(array) {
  let sum = 0;
  for (i = 0; i < array.length; i++) {
    sum += array[i];
  }
  return sum
}

/**
 * oddArray()
 * ---------------------
 * Returns a new array of only the odd numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  oddArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 11, 15, 37 ]
 */
function oddArray(array) {
  let odd = [];
  for(i = 0; i < array.length; i++){
    if((array[i] % 2) !== 0){
      odd.push(array[i]);
    }
  }
  return odd
}


/**
 * evenArray()
 * ---------------------
 * Returns a new array of only the even numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  evenArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 20, 22 ]
 */
function evenArray(array) {
let even = [];
  for (i = 0; i < array.length; i++) {
  if ((array[i] % 2) === 0) {
    even.push(array[i]);
    }
  } 
  return even
}


